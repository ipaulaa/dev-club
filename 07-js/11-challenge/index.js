function add(n1, n2) {
  console.log(`${n1} + ${n2} is ${n1 + n2}`);
}

function sub(n1, n2) {
  console.log(`${n1} - ${n2} is ${n1 - n2}`);
}

function mul(n1, n2) {
  console.log(`${n1} * ${n2} is ${n1 * n2}`);
}

function div(n1, n2) {
  console.log(`${n1} / ${n2} is ${n1 / n2}`);
}

function mod(n1, n2) {
  console.log(`${n1} % ${n2} is ${n1 % n2}`);
}

function pow(n1, n2) {
  console.log(`${n1} ** ${n2} is ${n1 ** n2}`);
}

add(3, 7);
sub(3, 7);
mul(3, 7);
div(7, 3);
mod(7, 3);
pow(7, 3);
