function add(n1, n2) {
  return n1 + n2;
}

function sub(n1, n2) {
  return n1 - n2;
}

function mul(n1, n2) {
  return n1 * n2;
}

function div(n1, n2) {
  return n1 / n2;
}

function pow(n1, n2) {
  return n1 ** n2;
}

function mod(n1, n2) {
  return n1 % n2;
}

module.exports = { add, sub, mul, div, pow, mod };
